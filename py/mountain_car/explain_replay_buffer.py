print("  - 导致训练不稳定，泛化能力差")
print()

print("=" * 70)
print("2. 解决方案：随机打乱")
print("=" * 70)
print()
print("使用 Replay Buffer 后：")
print("  Buffer: [经验₀, 经验₁, 经验₂, ..., 经验ₙ]")
print()
print("采样时：")
print("  Batch = [经验₁₂₅, 经验₇, 经验₅₃₁, 经验₂, ...]")
print()
print("优势：")
print("  ✓ 随机采样打破时序相关性")
print("  ✓ 每个批次数据独立同分布（I.I.D.）")
print("  ✓ 神经网络训练更稳定")
print()

