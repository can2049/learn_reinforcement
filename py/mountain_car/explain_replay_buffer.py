print("1. 问题：时序相关性（Temporal Correlation）")
print("=" * 70)
print()
print("没有 Replay Buffer 的情况：")
print("  t=0: state₀ → action₀ → reward₀ → state₁")
print("  t=1: state₁ → action₁ → reward₁ → state₂")
print("  t=2: state₂ → action₂ → reward₂ → state₃")
print("  ...")
print()
print("问题：")
print("  - 连续的经验高度相关（state₁ 和 state₂ 很相似）")
